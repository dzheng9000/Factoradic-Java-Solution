package permute;

/**
 *
 * @author Dave Zheng
 */
public class SpecialInt 
{
    private long value = 1;
    /**
     *Method increment increases number by 1.
     */
    public void increment()
    {
        value++;
    }
    /**
     *
     * @return the value of the number
     */
    public long getInt()
    {
        return value;
    }

}
